$(document).ready(function(){
    function getArticle(i) {
        for (var i = 0; i <= cosmeticos.length-1; i++) {
            var div = $('<div class="content_article '+cosmeticos[i].type+'"><div class="image_article"><img src="images/cosmeticos/'+cosmeticos[i].picture+'"></div><div class="designer_article">'+cosmeticos[i].designer+'</div><div class="name_article">'+cosmeticos[i].name+'</div><div class="price_article">$'+cosmeticos[i].price+'</div>');
            $("#article_content").append(div);
        }
        $(".cosmetics").hide();
        return i;
    }

    function showCosmetics() {
        $(".fem").hide();
        $(".male").hide();
        $(".makeup").hide();
        $(".cosmetics").show();
        $(".fem").removeClass("jp-hidden");
        $("div.holder").jPages({
            containerID  : "article_content",
            perPage      : 12,
            startPage    : 1,
            startRange   : 1,
            first       : false,
            previous    : false,
            next        : false,
            last        : false
        });
    }

    getArticle();
    $(".cosmetics").hide();
    $("#cosmeticos").on("mouseenter", getArticle);
    $("#cosmeticos").on("click", showCosmetics);
});