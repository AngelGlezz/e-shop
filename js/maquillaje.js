$(document).ready(function(){
    function getArticle(i) {
        for (var i = 0; i <= maquillaje.length-1; i++) {
            var div = $('<div class="content_article '+maquillaje[i].type+'"><div class="image_article"><img src="images/maquillaje/'+maquillaje[i].picture+'"></div><div class="designer_article '+maquillaje[i].designer+'">'+maquillaje[i].designer+'</div><div class="name_article">'+maquillaje[i].name+'</div><div class="price_article">$'+maquillaje[i].price+'</div>');
            $("#article_content").append(div);
        }
        $(".makeup").hide();
        $("#buscador").val("");
        return i;
    }

    function showMakeup() {
        $("#buscador").val("");
        $(".fem").hide();
        $(".male").hide();
        $(".makeup").show();
        $(".cosmetics").hide();
        $(".fem").removeClass("jp-hidden");
        $("div.holder").jPages({
            containerID  : "article_content",
            perPage      : 12,
            startPage    : 1,
            startRange   : 1,
            first       : false,
            previous    : false,
            next        : false,
            last        : false
        });
    }

    getArticle();
    $(".makeup").hide();
    $("#maquillaje").on("click", showMakeup);
});